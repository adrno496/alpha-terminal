export function getLocale() { return _locale; }

export function setLocale(loc) {
  if (!['fr', 'en'].includes(loc)) return;
  _locale = loc;
  localStorage.setItem(KEY, loc);
  document.documentElement.setAttribute('lang', loc);
  listeners.forEach(fn => fn(loc));
}

export function t(key, fallback) {
  return DICT[_locale]?.[key] || DICT.fr[key] || fallback || key;
}

export function onLocaleChange(fn) { listeners.add(fn); return () => listeners.delete(fn); }

export function languageSuffix() {
  return _locale === 'en'
    ? '\n\nIMPORTANT: Reply in English. Override any prior instruction to respond in French — your final output must be in English.'
    : '';
}

export function makeLocaleToggle() {
  const btn = document.createElement('button');
  btn.className = 'lang-toggle';
  btn.textContent = _locale.toUpperCase();
  btn.title = t('lang.toggle.tooltip');
  btn.addEventListener('click', () => {
    setLocale(_locale === 'fr' ? 'en' : 'fr');
    // Pas de location.reload() (crash dans Electron file://) — on dispatche un event
    // app.js le capte et re-render la sidebar + la vue active proprement
    window.dispatchEvent(new CustomEvent('app:locale-changed'));
  });
  return btn;
}

document.documentElement.setAttribute('lang', _locale);

// Helper : applique tous les data-i18n / data-i18n-html du DOM
export function applyI18nAttributes(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  root.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
}
// Auto-apply on locale change
onLocaleChange(() => applyI18nAttributes());
